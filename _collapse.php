<p>The collapse plugin utilizes a few classes to handle the heavy lifting:</p>
<p><code>.collapse</code> hides the content</p>
<p><code>.collapse.in</code> shows the content</p>
<p><code>.collapsing</code> is added when the transition starts, and removed when it finishes</p>
<p>Just add <code>data-toggle="collapse"</code> and a <code>data-target</code> to the element to automatically assign control of a collapsible element. The data-target attribute accepts a CSS selector to apply the collapse to. Be sure to add the class <code>collapse</code> to the collapsible element. If you'd like it to default open, add the additional class in.</p>
<p><a class="btn btn-primary" role="button" data-toggle="collapse" href="#collapseExample" aria-expanded="false" aria-controls="collapseExample">
  Link with href
</a>
<button class="btn btn-primary" type="button" data-toggle="collapse" data-target="#collapseExample" aria-expanded="false" aria-controls="collapseExample">
  Button with data-target
</button></p>

